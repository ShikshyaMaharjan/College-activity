# Create a list
nums = [10, 20, 30, 40, 50, 60]

# Indexing (access single elements)
print("First element:", nums[0])
print("Last element:", nums[-1])

# Slicing (access sublists)
print("First three elements:", nums[0:3])
print("Middle elements:", nums[2:5])
print("Last three elements:", nums[-3:])

# Step slicing
print("Every second element:", nums[::2])